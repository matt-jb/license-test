function compareDependencyLists(depsByTraversing, depsByNPM) {
  const traversingSet = new Set(depsByTraversing);
  const npmSet = new Set(depsByNPM);

  const onlyInTraversing = depsByTraversing.filter((dep) => !npmSet.has(dep));

  const onlyInNPM = depsByNPM.filter((dep) => !traversingSet.has(dep));

  return [onlyInTraversing, onlyInNPM];
}

